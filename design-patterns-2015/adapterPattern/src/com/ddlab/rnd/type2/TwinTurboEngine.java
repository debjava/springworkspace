package com.ddlab.rnd.type2;

public class TwinTurboEngine {

	public TwinTurboEngine() {

	}

	public void turnOn() {
		System.out.println("Turbo started");
	}

	public void turnOff() {
		System.out.println("Stopped immediately");
	}

	public void engage() {
		System.out.println("Hi Speed acceleration ...");
	}

	public void disengage() {
		System.out.println("Speed decreased immediately");
	}

	public void brake() {
		System.out.println("Sudden brake applied");
	}

}
